// Using While Loop to find Traverse an Array
let arr = [10, 20, 30, 40];
let a = 0;
while (a < arr.length) {
  console.log(arr[a]);
  a++;
}


// do-while loop that counts from 1 to 5.
let count = 1;
do {
  console.log(count);
  count++;
} while (count <= 5);